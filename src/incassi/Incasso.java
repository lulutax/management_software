package incassi;

public class Incasso {
	String data;
	float incasso;
	public Incasso(String data, float incasso) {
		super();
		this.data = data;
		this.incasso = incasso;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public float getIncasso() {
		return incasso;
	}
	public void setIncasso(float incasso) {
		this.incasso = incasso;
	}
	
	
	
}
